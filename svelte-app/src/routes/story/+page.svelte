    <h1>Story</h1>

    <br>
    <br>

    <p>A man, who collects odd and unusual books as a hobby, comes across an ancient tome.</p>
    <p>Upon opening the book, he is transported to a place with no light, no people, no buildings and no sound.</p>
    <p>He walks for some time before hearing voices, a figure approaches him from the darkness.</p>
    <p>Its face is obscured by a red cloak, only its teeth visible, it teaches him to fight and takes hostage over his mind.</p>
    <p>He walks and walks until he spots what looks like a coloseum.</p>
    <p>The Depth told him that if he becomes the new god then it will leave him alone and he will be able to return home.</p>
    <p>He learns that the god of these creations would inflict cruel punishments on them, having once been human, but now twisted and distorted.</p>
    <p>When he reaches the throne room, having defeated the fallen god's creations, the Depth speaks with him.</p>
    <p>It can't return him home, it too is trapped in this realm, but it will leave him alone.</p>
    <p>He tries to speak with it, ask it questions, but the entity disintegrates before his eyes.</p>
    <p>The man becomes trapped in this realm, the new god of the endless abyss.</p>
    <p>He creates people to keep him company in the darkness, his creations.</p>
    <p>But he soon grows tired of them, hateful, he twists and turns them into something new, something disfigured and broken.</p>
    <p>He wanted to leave this place and one day his prayers are answered.</p>
    <p>He sees a person stumbling through the void.</p>
    <p>He smiles to himself, the red cloak obscuring his face so only his mouth is visible.</p>
    <p>He promises the person that if he claims the throne, as the new god, then he will send him home.</p>

